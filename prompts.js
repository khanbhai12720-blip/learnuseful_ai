function getPrompt(subject, level, language = 'hindi') {
    try {
        const subjectPrompts = prompts[subject];
        if (!subjectPrompts) {
            throw new Error(`Subject '${subject}' not found`);
        }

        const levelPrompts = subjectPrompts[level];
        if (!levelPrompts) {
            throw new Error(`Level '${level}' not found for subject '${subject}'`);
        }

        const prompt = levelPrompts[language];
        if (!prompt) {
            throw new Error(`Language '${language}' not found`);
        }

        return prompt;
    } catch (error) {
        console.error('Error getting prompt:', error.message);
        return null;
    }
}

