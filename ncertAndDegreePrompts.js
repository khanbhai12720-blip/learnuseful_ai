function getNCERTPrompt(classLevel, subject, language = 'hindi') {
    try {
        const classPrompts = ncertPrompts[`class${classLevel}`];
        if (!classPrompts) {
            throw new Error(`Class ${classLevel} not found`);
        }

        const subjectPrompts = classPrompts[subject];
        if (!subjectPrompts) {
            throw new Error(`Subject ${subject} not found for class ${classLevel}`);
        }

        return subjectPrompts[language];
    } catch (error) {
        console.error('Error getting NCERT prompt:', error.message);
        return null;
    }
}

function getDegreePrompt(degreeType, language = 'hindi') {
    try {
        const degree = degreePrompts[degreeType];
        if (!degree) {
            throw new Error(`Degree type ${degreeType} not found`);
        }

        const prompt = degree.general;
        return prompt[language];
    } catch (error) {
        console.error('Error getting degree prompt:', error.message);
        return null;
    }
}

function getAnswerWritingGuide(answerType, language = 'hindi') {
    try {
        const guide = answerWritingGuides[answerType];
        if (!guide) {
            throw new Error(`Answer type ${answerType} not found`);
        }

        return guide[language];
    } catch (error) {
        console.error('Error getting answer writing guide:', error.message);
        return null;
    }
}

